export default function LessonsPage() {
  const searchParams = useSearchParams()
  const level = (searchParams.get("level") as "basic" | "intermediate" | "advanced") || "basic"

  const [currentIndex, setCurrentIndex] = useState(0)
  const [showTranslation, setShowTranslation] = useState(false)
  const [learnedCards, setLearnedCards] = useState<Set<string>>(new Set())
  const [isFlipped, setIsFlipped] = useState(false)
  const [selectedCategory, setSelectedCategory] = useState<string>("all")

  const [currentPage, setCurrentPage] = useState(1)
  const cardsPerPage = 10

  // Filter flashcards by level and category
  const levelCards = flashcardData.filter((card) => card.level === level)
  const filteredCards =
    selectedCategory === "all" ? levelCards : levelCards.filter((card) => card.category === selectedCategory)

  // Get unique categories for the current level
  const categories = ["all", ...Array.from(new Set(levelCards.map((card) => card.category)))]

  // Pagination
  const totalPages = Math.ceil(filteredCards.length / cardsPerPage)
  const startIndex = (currentPage - 1) * cardsPerPage
  const currentCards = filteredCards.slice(startIndex, startIndex + cardsPerPage)
  const currentCard = currentCards[currentIndex]

  // Load learned cards from localStorage
  useEffect(() => {
    const saved = localStorage.getItem(`learned-cards-${level}`)
    if (saved) {
      setLearnedCards(new Set(JSON.parse(saved)))
    }
  }, [level])

  // Save learned cards to localStorage
  useEffect(() => {
    localStorage.setItem(`learned-cards-${level}`, JSON.stringify([...learnedCards]))
  }, [learnedCards, level])

  // Reset current index when category or page changes
  useEffect(() => {
    setCurrentIndex(0)
    setIsFlipped(false)
    setShowTranslation(false)
  }, [selectedCategory, currentPage])

  const speakText = (text: string) => {
    if ("speechSynthesis" in window) {
      const utterance = new SpeechSynthesisUtterance(text)
      utterance.lang = "en-US"
      utterance.rate = 0.8
      speechSynthesis.speak(utterance)
    }
  }

  const nextCard = () => {
    if (currentIndex < currentCards.length - 1) {
      setCurrentIndex(currentIndex + 1)
      setIsFlipped(false)
      setShowTranslation(false)
    } else if (currentPage < totalPages) {
      setCurrentPage(currentPage + 1)
    }
  }

  const prevCard = () => {
    if (currentIndex > 0) {
      setCurrentIndex(currentIndex - 1)
      setIsFlipped(false)
      setShowTranslation(false)
    } else if (currentPage > 1) {
      setCurrentPage(currentPage - 1)
      setCurrentIndex(cardsPerPage - 1)
    }
  }

  const toggleLearned = () => {
    if (!currentCard) return

    const newLearnedCards = new Set(learnedCards)
    if (learnedCards.has(currentCard.id)) {
      newLearnedCards.delete(currentCard.id)
    } else {
      newLearnedCards.add(currentCard.id)
    }
    setLearnedCards(newLearnedCards)
  }

  const flipCard = () => {
    setIsFlipped(!isFlipped)
    setShowTranslation(!showTranslation)
  }

  const resetProgress = () => {
    setLearnedCards(new Set())
    localStorage.removeItem(`learned-cards-${level}`)
  }

  if (!currentCard) {
    return (
      <div className="min-h-screen bg-gradient-to-br from-blue-50 to-green-50">
        <Navigation />
        <div className="container mx-auto px-4 py-8">
          <div className="text-center">
            <h1 className="text-3xl font-bold mb-4">Nenhuma lição encontrada</h1>
            <p>Tente selecionar uma categoria diferente.</p>
          </div>
        </div>
      </div>
    )
  }

  const levelTitles = {
    basic: "Básico",
    intermediate: "Intermediário",
    advanced: "Avançado",
  }

  const progress = Math.round((learnedCards.size / levelCards.length) * 100)

  return (
    <div className="min-h-screen bg-gradient-to-br from-blue-50 to-green-50">
      <Navigation />

      <div className="container mx-auto px-4 py-8">
        <div className="max-w-4xl mx-auto">
          {/* Header */}
          <div className="text-center mb-8">
            <h1 className="text-4xl font-bold text-gray-800 mb-2">Flashcards - Nível {levelTitles[level]}</h1>
            <p className="text-gray-600 mb-4">
              {filteredCards.length} lições disponíveis • {learnedCards.size} aprendidas
            </p>

            {/* Progress Bar */}
            <div className="w-full bg-gray-200 rounded-full h-3 mb-6">
              <div
                className="bg-gradient-to-r from-green-400 to-blue-500 h-3 rounded-full transition-all duration-300"
                style={{ width: `${progress}%` }}
              ></div>
            </div>
            <p className="text-sm text-gray-600">{progress}% concluído</p>
          </div>

          {/* Category Filter */}
          <div className="mb-6">
            <div className="flex flex-wrap gap-2 justify-center">
              <Button
                variant={selectedCategory === "all" ? "default" : "outline"}
                size="sm"
                onClick={() => {
                  setSelectedCategory("all")
                  setCurrentPage(1)
                }}
                className="text-xs"
              >
                <Filter className="w-3 h-3 mr-1" />
                Todas
              </Button>
              {categories.slice(1).map((category) => (
                <Button
                  key={category}
                  variant={selectedCategory === category ? "default" : "outline"}
                  size="sm"
                  onClick={() => {
                    setSelectedCategory(category)
                    setCurrentPage(1)
                  }}
                  className="text-xs"
                >
                  {category}
                </Button>
              ))}
            </div>
          </div>

          {/* Pagination Info */}
          <div className="text-center mb-4">
            <p className="text-sm text-gray-600">
              Página {currentPage} de {totalPages} • Lição {startIndex + currentIndex + 1} de {filteredCards.length}
            </p>
          </div>

          {/* Flashcard */}
          <div className="mb-8">
            <Card className="max-w-2xl mx-auto">
              <CardHeader className="text-center">
                <div className="flex justify-between items-center mb-2">
                  <Badge variant="secondary">{currentCard.category}</Badge>
                  <Badge
                    variant={
                      currentCard.type === "vocabulary"
                        ? "default"
                        : currentCard.type === "grammar"
                          ? "destructive"
                          : "outline"
                    }
                  >
                    {currentCard.type === "vocabulary"
                      ? "Vocabulário"
                      : currentCard.type === "grammar"
                        ? "Gramática"
                        : "Expressão"}
                  </Badge>
                </div>
                <CardTitle className="text-2xl">{isFlipped ? currentCard.translation : currentCard.english}</CardTitle>
              </CardHeader>

              <CardContent className="text-center space-y-4">
                {showTranslation && (
                  <div className="p-4 bg-gray-50 rounded-lg">
                    <p className="text-lg font-medium text-gray-700 mb-2">
                      {isFlipped ? currentCard.english : currentCard.translation}
                    </p>
                    {currentCard.example && (
                      <p className="text-sm text-gray-600 italic">
                        <strong>Exemplo:</strong> {currentCard.example}
                      </p>
                    )}
                  </div>
                )}

                <div className="flex justify-center gap-2">
                  <Button onClick={flipCard} variant="outline" size="sm">
                    <RotateCcw className="w-4 h-4 mr-2" />
                    Virar
                  </Button>
                  <Button onClick={() => speakText(currentCard.english)} variant="outline" size="sm">
                    <Volume2 className="w-4 h-4 mr-2" />
                    Ouvir
                  </Button>
                  <Button
                    onClick={toggleLearned}
                    variant={learnedCards.has(currentCard.id) ? "default" : "outline"}
                    size="sm"
                  >
                    <CheckCircle className="w-4 h-4 mr-2" />
                    {learnedCards.has(currentCard.id) ? "Aprendida" : "Marcar"}
                  </Button>
                </div>
              </CardContent>
            </Card>
          </div>

          {/* Navigation */}
          <div className="flex justify-between items-center mb-6">
            <Button onClick={prevCard} disabled={currentIndex === 0 && currentPage === 1} variant="outline">
              <ArrowLeft className="w-4 h-4 mr-2" />
              Anterior
            </Button>

            <div className="flex gap-2">
              <Button onClick={resetProgress} variant="outline" size="sm">
                Resetar Progresso
              </Button>
            </div>

            <Button
              onClick={nextCard}
              disabled={currentIndex === currentCards.length - 1 && currentPage === totalPages}
            >
              Próxima
              <ArrowRight className="w-4 h-4 ml-2" />
            </Button>
          </div>

          {/* Page Navigation */}
          <div className="flex justify-center gap-2">
            {Array.from({ length: Math.min(5, totalPages) }, (_, i) => {
              const pageNum = Math.max(1, Math.min(totalPages - 4, currentPage - 2)) + i
              return (
                <Button
                  key={pageNum}
                  variant={currentPage === pageNum ? "default" : "outline"}
                  size="sm"
                  onClick={() => setCurrentPage(pageNum)}
                >
                  {pageNum}
                </Button>
              )
            })}
          </div>
        </div>
      </div>
    </div>
  )
}
